<p>vormt een groene oase in de woonwijk, waar de wijken de Bunders en ’t Ven elkaar ontmoeten. Wijkbewoners wandelen hier om de dieren te begroeten of om            de kruiden te zien bloeien.</p> 


<p>Op woensdagmiddag weten al veel kinderen en begeleiders dat er van alles te doen is. Ze zaaien groenten in de moestuin, knutselen, leren om te gaan met de dieren.</p>

<p>Op donderdagmiddag staat van mei – oktober de koffie klaar in ’t Bundertje. Dan zijn de gastvrouwen actief die bezoekers uit de wijk ontvangen. Ook cliënten van de Watersteeg kunnen met hun bezoek hier genieten van de bloeiende tuinen en de dieren.</p>

<p>Voor basisscholen, BSO en kinderopvangorganisaties zijn er lesmaterialen te leen bij de natuurtuin. Het aanbod van leskisten en onderzoekmaterialen voor diverse leeftijden wordt elk jaar verstuurd naar de basisscholen. Deze lijst is ook voor andere organisaties op te vragen bij de natuurtuin.</p>

<p>De Gemeente Veghel heeft hier een centrum voor natuureducatie ingericht. Kinderen leren over de planten en dieren in de tuin. Vrijwilligers uit de wijk doen klussen, begeleiden kinderactiviteiten, schenken koffie en denken mee over de inrichting van de tuin. Hieronder ziet u een greep uit de mogelijkheden, nieuwe ideeën zijn welkom - doet u ook mee!</p>

<p>In de kruidentuin plukken wij de kruiden om thee te zetten en voor kookprojecten. Met geurdoosjes laten we raden welke kruiden het zijn, een mooie kennismaking met verse kruiden.
De moestuin wordt ingeplant door de kinderen op de woensdagmiddag. Bij het oogsten van de diverse groenten zijn er ook recepten die gelijk worden gemaakt door de groepen. Kinderen proeven dan gelijk wat ze hebben verbouwd.
De vlindertuin bloeit en biedt een mooie plek om diverse insecten te ontmoeten. Met zoekkaarten kunnen de kinderen kijken wat er allemaal vliegt.
Een hooiakkertje wordt beheerd door vrijwilligers, de geiten en konijnen genieten dan van verse bloemrijk hooi. Op mooie dagen mogen de kinderen een van de geiten meenemen naar de wei.
Op woensdagmiddag wordt er geknutseld, getekend en geschilderd. Inspiratie voor alle kunstnijverheid wordt gezocht bij de dieren en in de tuinen, waardoor ware natuurkunstenaars zich aardig uit kunnen leven.</p> 

